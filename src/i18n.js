const savedLocale = localStorage.getItem('market-orca:locale') || 'id'
const locale = reactive({ value: savedLocale })

function setLocale(loc) {
  locale.value = loc
  localStorage.setItem('market-orca:locale', loc)
  document.documentElement.setAttribute('lang', loc)
}

function t(key, fallback) {
  const val = translations[locale.value]?.[key] ?? translations['id']?.[key] ?? fallback ?? key
  return val
}

// Initialize lang attr
document.documentElement.setAttribute('lang', locale.value)

export { translations, locale, setLocale, t }
export default { translations, locale, setLocale, t }
